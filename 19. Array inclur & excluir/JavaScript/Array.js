var listas_frutas = Array ()

listas_frutas[0] = 'Banana' 
listas_frutas[1] = 'Maçã' 
listas_frutas[2] = 'Manga' 
listas_frutas[3] = 'Uva' 
listas_frutas[4] = 'Melancia' 

console.log(listas_frutas)


//incluir um elemento no final do Array 

listas_frutas.push('Kiwi')
console.log(listas_frutas)

//incluir um elemento no início do Array

listas_frutas.unshift('Tomate')
console.log(listas_frutas)

//excluir um elemento no Final do array

listas_frutas.pop()
console.log(listas_frutas)

//excluir um elemento no Início do array

listas_frutas.shift()
console.log(listas_frutas)

